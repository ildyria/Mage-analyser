<span class="utitle" id="I">I) WORLD of LOGS</span><br />
<span class="mtitle" id="I1">1) O&ugrave; puis je voir mes logs ?</span><br />
<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/realm10.png" alt="Image" /><br />
Tout d'abord sur la pages principale de WoL, s&eacute;lectionnez un royaume.<br />
<br />
On arrive alors sur la page suivante : <br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/realm210.png" alt="Image" /><br />
S&eacute;lectionnez votre serveur.<br />
<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/guild10.png" alt="Image" /><br />
S&eacute;lectionnez la guilde dont vous vouler voir les logs ou votre guilde.<br />
<br />
Vous arrivez au calendrier du mois en cours.<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/calend10.jpg" alt="Image" /><br />
Ici nous allons analyser la derni&egrave;re performance d'un mage de ma guilde Jamiro, nous cliquons donc sur le dernier log disponible.<br />
<br />
On arrive alors sur cette page qui est un r&eacute;sum&eacute; de la soir&eacute;e.<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/main10.png" alt="Image" /><br />
<br />
<span class="mtitle" id="I2">2) Rechercher des informations utiles</span><br />
<br />
Allons maintenant chercher des informations.<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/select10.png" alt="Image" /><br />
On s&eacute;lectionne par exemple Balerok  25N. Soit par le menu d&eacute;roulant, soit en cliquant directement sur le lien vers le combat &agrave; analyser.<br />
<br />
On arrive alors sur la page qui r&eacute;sume le combat.<br />
Ici on a trois choix, soit regarder d'abord, la personne qui nous int&eacute;resse, soit on va voir le damage done du raid au cours du temps ou soit on va dans le Log Browser.<br />
<br />
<span class="mtitle" id="I3">3) le r&eacute;sum&eacute; personnel</span><br />
<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/fight10.png" alt="Image" /><br />
Pour commencer nous allons aller voir le r&eacute;sum&eacute; de Jamiro sur ce combat.<br />
<br />
On arrive alors sur le graph suivant qui n'est pas tr&egrave;s explicite, aussi vous pouvez voyager dans les onglets et acc&eacute;der &agrave; des r&eacute;sum&eacute;s plus d&eacute;taill&eacute;s.<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/select11.png" alt="Image" /><br />
<br />
En cliquant sur Damage by Spell on acc&egrave;de aux sorts de d&eacute;g&acirc;ts lanc&eacute;s pendant le combat.<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/damage10.png" alt="Image" /><br />
Entour&eacute; en jaune correspond &agrave; la zone qui nous int&eacute;resse quand on est en sp&eacute; feu. Car elle indique l'up time (le temps effectif o&ugrave; le dot est pos&eacute; pendant le combat) des Bombes vivantes et autres.<br />
<br />
En cliquant sur l'onglet Damage by actor, vous acc&eacute;derez aux d&eacute;g&acirc;ts faits par Jamiro sur ses cibles, ça peut &ecirc;tre int&eacute;ressant pour voir combien vous avez fait sur les blop de malo en phase noire, sur ses add ou alors &agrave; l'&eacute;poque de WotLK sur les b&ecirc;tes de Saurcroc.<br />
<br />
L'onglet Death nous dit comment il est mort dans le cas ou la personne regard&eacute;e est morte.<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/death10.png" alt="Image" /><br />
Visiblement, Jami est mort sur ce combat...<br />
<br />
<span class="mtitle" id="I4">4) le raid damage done </span><br />
<br />
Pour y acc&eacute;der il suffit d'utiliser le menu du haut et d'aller chercher la ligne &quot;Damage Done&quot;<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/select12.png" alt="Image" /><br />
<br />
On arrive alors sur cette page, par d&eacute;faut seuls les 3 plus haut au d&eacute;g&acirc;ts inflig&eacute;s sont affich&eacute;s. Nous allons donc afficher la ligne de Jamiro en cochant sa case.<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/damage11.png" alt="Image" /><br />
<br />
Le graph des d&eacute;g&acirc;ts qu'il inflige en fonction du temps s'affiche alors en haut.<br />
Grace &agrave; ce tableau et graph, on acc&egrave;de &agrave; des informations pr&eacute;cises et instructives.<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/analys10.png" alt="Image" /><br />
Ici on voit que &agrave; 21h38 et 16 secondes Jamiro est mort (bah oui, c'est comme l'&eacute;lectro cardiogramme, quand ça bouge plus, le coeur bat plus =D). On voit qu'il a un active time de 37%. L'active time, c'est le temps o&ugrave; vous faites quelque chose pendant le combat. Un bon joueur doit avoir un active time supp&eacute;rieur &agrave; 95%, mais vu qu'il est mort l'active time baisse...<br />
Si apr&egrave;s un combat vous &ecirc;tes en vie et que votre active time est inf&eacute;rieur &agrave; 90%, c'est que vous avez merd&eacute; quelque part. :)<br />
On a aussi un ptit r&eacute;sum&eacute; de la proportion de d&eacute;g&acirc;ts effect&eacute;s par sorts utilis&eacute;s.<br />
<br />
<span class="mtitle" id="I5">5) La Log analyse</span><br />
<br />
On attaque maintenant la partie la plus complexe de l'analyse de log. Celle du log. On va donc se promener dans les diverses actions effectu&eacute;es par les joueurs et voir si y a pas des trucs bizzares. Pour un druide par exemple, on v&eacute;rifiera qu'il met bien sa r&eacute;tab au timer, qu'il &eacute;crase pas ses HoT, etc... Pour Jamiro, on va regarder ses s&eacute;quences d'entamme, quand est-ce qu'il claque l'orbe etc... <br />
<br />
Pour acc&eacute;der au Log Browser, c'est de le m&ecirc;me menu que Damage Done : <br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/log_br10.png" alt="Image" /><br />
<br />
On arrive donc sur la page suivante : <br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/log_br11.png" alt="Image" /><br />
Comme vous pouvez le voir elle est divis&eacute;e en deux parties : <br />
- la zone de requ&ecirc;tes<br />
- la zone de logs<br />
C'est dans la zone de logs que vous s'afficher les informations int&eacute;ressantes. Par d&eacute;faut, elle affiche tous les &eacute;venements (events). On cliquera donc sur <span style="font-style:italic">remove</span> pour &eacute;viter de poluer nos informations.<br />
<br />
Cr&eacute;ons maintenant une requete. Cliquez sur &quot;Add Query&quot; et vous allez avoir une petite fen&ecirc;tre qui va s'ouvrir.<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/add_qu10.png" alt="Image" /><br />
Ici on va demander au d'afficher les sorts lanc&eacute;s par Jamiro. On coche Spell Cast et on entre &quot;Jamiro&quot; dans source.<br />
Pour afficher les d&eacute;g&acirc;ts faits par Jamiro, il faudrait cocher Hit.<br />
Miss correspond aux rat&eacute;s.<br />
Heal, les soins effectu&eacute;s.<br />
Aura, c'est tout les buffs et d&eacute;buffs que vous allez gagner / perdre.<br />
Death, la mort quoi...<br />
Power Gain correspond &agrave; la mana / rage etc gagn&eacute;e.<br />
Vous pouvez aussi affiner la recherche en entrant que le nom du sort que vous voulez que la requ&ecirc;te affiche. Par exemple pour afficher que les d&eacute;flagration des arcanes cast&eacute; par Jamiro, il suffirait d'ajouter &quot;Arcane Blast&quot; (le log est en anglais ici, parfois il est en français, parfois en allemand, ça d&eacute;pend de la langue du jeu de celui qui prend le log.) dans la case Spell.<br />
<br />
Une fois que vous avez fait votre requete, cliquez sur Save.<br />
<br />
Vous pouvez lancer plusieurs requ&ecirc;tes en m&ecirc;me temps si vous voulez afficher plusieurs choses &agrave; la fois, par exemple, les d&eacute;buff de d&eacute;flagration des arcanes par rapport aux cast. Les proc de projectiles des arcanes etc.<br />
<br />
Une fois votre liste de requ&ecirc;tes faites, cliquez sur RUN.<br />
<br />
Alors s'affiche le r&eacute;sultat de votre requ&ecirc;te. Dans notre cas, cela donne :<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/log10.png" alt="Image" /><br />
<br />
Que peut on voir de ce log ? <br />
- Jamiro a lanc&eacute; la distorsion temporelle (Time Warp) alors qu'il &eacute;tait sous AP. C'est mal. Il faut rien lancer sous AP a part des d&eacute;flagration.<br />
<div style="border-top: 1px solid #0099CC; border-left: 1px solid #0099CC; border-right: 1px solid #0099CC; font-weight:bold; width: 500px; text-align:left; margin-left:auto; margin-right:auto; margin-top: 5px; padding: 5px; padding-bottom: 0px;">Citation:</div>
<div style="border-bottom: 1px solid #0099CC; border-left: 1px solid #0099CC; border-right: 1px solid #0099CC; width: 500px; text-align:left; margin-left:auto; margin-right:auto; padding: 5px; padding-top: 0px;">[23:36:06.517] Jamiro casts Arcane Power<br />
[23:36:06.966] Jamiro casts Replenish Mana<br />
[23:36:07.617] Jamiro casts Time Warp<br />
[23:36:08.613] Jamiro casts Mirror Image<br />
[23:36:09.556] Jamiro begins to cast Arcane Blast</div>
<br />
On voit ici qu'il a perdu 2,6 secondes d'AP &agrave; faire des choses inutiles.<br />
Sinon on voit qu'il respecte la gestion de la mana par phase etc... Apr&egrave;s ce qu'il faut regarder, c'est s'il ne perd pas de GCD, s'il utilise des barrages des arcanes &agrave; chaque d&eacute;placement, s'il lance bien 5 projectiles et pas 4 etc.<br /> 
