<br />
<span class="utitle" id="II">II) Mage Analyser</span><br />
<br />
<span class="mtitle" id="II1">1) Qu'est ce que c'est ?</span><br />
<br />
Mage analyser, c'est un analyser de log comme le Log Browser de WoL mais qui met en avant les erreurs personnelles : projectiles des arcanes perdus, GCD non utilis&eacute;s, utilisation des proc id&eacute;e claires, compte de sorts utilis&eacute;s : blink, trait de feu... Il vous donne aussi approximativement l'affichage de votre mana en fonction de l'avancement du combat.<br />
<br />
<span class="mtitle" id="II2">2) Ce que ne fait pas Mage Analyser</span><br />
<br />
Mage analyser ne fonctionne qu'avec la sp&eacute; arcane pour le moment. Pour ce qui est des sp&eacute; feu et givre il faudra attendre que je sorte les modules associ&eacute;s.<br />
<br />
<span class="mtitle" id="II3">3) Comment l'utiliser ?</span><br />
<br />
Allez &agrave; l'adresse <a href="http://wisdom.biv.free.fr/WoW/Mage%20analyser/">http://wisdom.biv.free.fr/WoW/Mage%20analyser/</a><br />
<br />
Dans la partie de droite vous avez une liste d'options.<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/ma110.png" alt="Image" /><br />
Nom du personnage : Jamiro<br />
On entre les caract&eacute;ristiques de notre ami de sa fiche armurie : <br />
- 1238 au score de hate<br />
- 4985 d'intel sur l'armurie, soit donc en raid 4985 x 1,05 (BoK/papate) = 5235 intel (les flacons 300 int et la nourriture 80 int sont ajout&eacute;s dans les calculs de l'analyser).<br />La langue du rapport, en l'occurence ici, le rapport est en anglais, donc on s&eacute;lectionne anglais.<br />
Ce qui nous donne :<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/ma210.png" alt="Image" /><br />
Puis on clique sur &quot;Requete World of Log&quot;.<br />
Alors apparait la ligne : <br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/ma310.png" alt="Image" /><br />
On copie la requ&ecirc;te (pensez &agrave; ctrl + A car elle est longue) :<br />
<span style="font-style:italic;">((sourceName = &quot;Jamiro&quot; and (((spellId = 55342 or spellId = 91173 or spellId = 97007 or spellId = 91047 or spellId = 92318 or spellId = 91007 or spellId = 89091 or spellId = 91019 or spellId = 92320 or spellId = 91024 or spellId = 12042 or spellId = 543 or spellId = 130 or spellId = 1953 or spellId = 12042 or spellId = 12536) and fullType = SPELL_AURA_APPLIED) or (spellId != 89091 and fullType = SPELL_DAMAGE))) or (targetName = &quot;Jamiro&quot; and (spellId = 2825 or spellId = 80353 or spellId = 90355 or spellId = 32182))) or (targetName = &quot;Jamiro&quot; and ((spellId = 12051 and fullType = SPELL_PERIODIC_ENERGIZE) or spellId = 5405 or spellId = 29166))</span><br />
<br />
Puis on retourne sur World of Logs, on s&eacute;lectionne le combat &agrave; analyser, et on va dans la page 
<span style="font-style:italic;">Expression Editor</span> :<br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/ma410.png" alt="Image" /><br />
<br />
Collez la requ&ecirc;te que vous aviez copi&eacute; dans la zone de text : <br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/ma510.png" alt="Image" /><br />
Et cliquez sur RUN<br />
<br />
Copiez coller le r&eacute;sultat de la requete dans la partie Log de Mage Analyser <br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/ma611.png" alt="Image" /><br />
N'oubliez pas de prendre tous le log, car il est souvent r&eacute;parti sur plusieurs pages, ne laissez pas de retour &agrave; la ligne &agrave; la fin du log car ça fait planter l'affichage du graphique.<br />
<br />
Et voila le r&eacute;sultat : <br />
<img src="http://i40.servimg.com/u/f40/11/00/34/63/ma710.png" alt="Image" /><br />
La colonne cast correspond au temps de cast du sort utilis&eacute;.<br />
Time lost correpond au temps perdu entre l'action de la ligne et la ligne pr&eacute;c&eacute;dente (moins le temps de cast).<br />
&Delta;mana correspond &agrave; la mana utilis&eacute;e ou gagn&eacute;e.<br />
Mana correspond &agrave; la mana du mage (les proc intel n'ont pas d'influence dessus).<br />
%Mana correspond au rapport mana actuelle sur mana totale (hors proc intel).<br />
<br />
Apr&egrave;s il suffit de lire et de tirer les conclusions qu'on souhaite. :)<br />
